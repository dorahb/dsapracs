def solution(number):
    if number % 3 == 0 and number % 5 == 0:
        return "FizzBuzz"
    elif number % 3 == 0:
        return "Fizz"
    elif number % 5 == 0:
        return "Buzz"
    else:
        return None

number = int(input("Enter a number: "))
result = solution(number)
print("Result:", result)
